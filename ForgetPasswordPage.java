    // input and try to reset the users password if the account exists
    private void resetPasswordBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetPasswordBtnActionPerformed
        String username = userNameTF.getText().trim();
        String newPassword = new String (passwordF.getPassword());
        
         // Check for empty fields
        if (username.isEmpty() || newPassword.isEmpty()){
            lblMessage.setText("Please fill in all fields!");
            return;
        }
        
        // Try to reset the password by UserAuth
        if (auth.resetPassword(username, newPassword)){
            int confirm = JOptionPane.showConfirmDialog(this, 
                "Are you sure want to reset the password?",
                "Confirm Password reset", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(this, "Password reset successfully!");
                dispose();
                new LoginPage(auth).setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "The account has not been registered yet.");
            userNameTF.setText("");
            passwordF.setText("");
        }
    }//GEN-LAST:event_resetPasswordBtnActionPerformed

